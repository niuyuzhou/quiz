package demo.entity;

/**
 * The type Response entity.
 */
public class ResponseEntity {
    private String name;
    private int number;

    /**
     * Gets name.
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets number.
     * @return the number
     */
    public int getNumber() {
        return number;
    }

    /**
     * Sets number.
     * @param number the number
     */
    public void setNumber(int number) {
        this.number = number;
    }
}
